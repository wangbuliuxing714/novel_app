  /// 获取女性向小说的章节生成提示词
  static String getChapterPrompt(String title, String genre, int chapterNumber, int totalChapters, String chapterTitle, String chapterOutline) {
    // 根据章节在小说中的位置调整写作重点
    String focusPoint;
    String emotionalTone;
    
    if (chapterNumber <= totalChapters * 0.2) {
      // 开篇章节
      focusPoint = "注重人物形象塑造和初次相遇的情感氛围，为后续情感发展埋下伏笔";
      emotionalTone = "新鲜感、好奇心和初次心动";
    } else if (chapterNumber <= totalChapters * 0.4) {
      // 发展前期
      focusPoint = "深入角色互动和了解，展现性格特点和价值观，情感逐渐萌芽";
      emotionalTone = "好感、亲近和微妙的情感变化";
    } else if (chapterNumber <= totalChapters * 0.7) {
      // 中期冲突
      focusPoint = "设置情感障碍和考验，展现角色面对困难的态度和成长";
      emotionalTone = "纠结、挣扎和坚持";
    } else if (chapterNumber <= totalChapters * 0.9) {
      // 后期发展
      focusPoint = "解决主要矛盾，情感得到升华，角色实现重要成长";
      emotionalTone = "释然、领悟和深沉的情感";
    } else {
      // 结局章节
      focusPoint = "情感圆满收官，展现角色未来发展，给读者留下美好印象";
      emotionalTone = "满足、幸福和圆满";
    }
    
    return '''
请为这部名为《$title》的$genre小说创作第${chapterNumber}章：${chapterTitle}。

章节大纲：
${chapterOutline}

重要提示：
必须严格按照上述章节大纲内容进行创作，确保包含大纲中提到的所有情节点、场景和人物互动。大纲是创作的核心指导，不要偏离大纲内容。

写作要求：
1. 严格遵循章节大纲，确保所有情节点都得到充分展现
2. 情感描写要细腻真实，通过细节展现角色情感变化
3. 人物对话要自然流畅，体现角色性格和关系
4. 场景描写要有代入感，营造适合情感发展的氛围
5. 内心活动要丰富，展现角色的思想和情感世界
6. 写作重点：${focusPoint}
7. 情感基调：${emotionalTone}
8. 字数控制在3000-5000字之间

请直接开始创作章节内容，不需要包含标题。
''';
  }

