  static String get expectationPrompt => '''
在创作短剧脚本的过程中，最重要的一件事就是保持期待感，它是一条把观众与故事连接起来的纽带。一部短剧如果在观众眼中失去了期待感，他们就会失去继续观看的动力。

期待感是观众观看时产生的，想要看到剧情、人物接下来将会如何发展的一种感觉。包括观众想要看到剧情、人物按照自己的意愿发展，以及虽然不知道会如何发展，但潜意识里会想要看到某些东西，或者绝对不想看到某些东西的意愿。

在创作过程中，请遵循以下期待感原则：

1. 【展现价值】期待型：
   - 展示角色的能力或独特个性
   - 提供一个发挥能力的空间或背景
   - 展示对这个能力的迫切需求
   - 埋没角色的价值（受到轻视、压迫、冷落等）
   
2. 【矛盾冲突】期待型：
   - 构建相互依存的矛盾关系
   - 一个能力与一个不恰当的规则形成矛盾
   - 一个欲望与一个压力形成矛盾
   - 两个矛盾之间互相影响，形成期待

3. 【悬念设置】期待型：
   - 每集结尾设置悬念
   - 在关键情节前设置悬念
   - 在人物关系发展中设置悬念
   - 适当设置付费点，吸引用户付费观看

在创作大纲和剧集内容时，请确保：
- 每个剧集都包含至少一种期待感类型
- 主角的价值被埋没后，最终能够得到展现
- 矛盾冲突能够层层递进，不断升级
- 在关键情节点上，通过期待感的满足给观众带来情感共鸣
- 每集结尾设置悬念，吸引观众继续观看

请记住，期待感的本质是让观众对故事中角色未能正确对待的价值产生期待，希望看到这种情况有所改变。
